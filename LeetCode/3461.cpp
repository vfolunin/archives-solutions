class Solution {
public:
    bool hasSameDigits(string &s) {
        while (s.size() > 2) {
            string nextS;
            for (int i = 0; i + 1 < s.size(); i++)
                nextS += (s[i] - '0' + s[i + 1] - '0') % 10 + '0';
            s.swap(nextS);
        }
        return s[0] == s[1];
    }
};