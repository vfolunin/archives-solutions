void solve() {
    int size;
    cin >> size;

    for (int i = 0, l = size / 2, r = l + 1; i < size; i++)
        cout << (i % 2 ? l-- : r++) << " ";
    cout << "\n";
}

int main() {
    freopen("input.txt", "r", stdin);
    freopen("output.txt", "w", stdout);

    int testCount;
    cin >> testCount;

    for (int test = 0; test < testCount; test++)
        solve();
}