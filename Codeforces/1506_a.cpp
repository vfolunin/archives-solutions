using namespace std;

void solve() {
    long long h, w, n;
    cin >> h >> w >> n;

    n--;
    long long col = n / h;
    long long row = n % h;

    cout << row * w + col + 1 << "\n";
}

int main() {
    freopen("input.txt", "r", stdin);
    freopen("output.txt", "w", stdout);

    int testCount;
    cin >> testCount;

    for (int test = 0; test < testCount; test++)
        solve();
}