void solve() {
    int n;
    cin >> n;

    for (int i = 1; i <= n; i += 2)
        for (int j = i; j <= n; j *= 2)
            cout << j << " ";
    cout << "\n";
}

int main() {
    freopen("input.txt", "r", stdin);
    freopen("output.txt", "w", stdout);

    int testCount;
    cin >> testCount;

    for (int test = 0; test < testCount; test++)
        solve();
}