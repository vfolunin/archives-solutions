void solve() {
    string s;
    cin >> s;

    for (int i = 0; i < s.size(); i++) {
        if (i && isupper(s[i]))
            cout << "_";
        cout << (char)tolower(s[i]);
    }
    cout << "\n";
}

int main() {
    freopen("input.txt", "r", stdin);
    freopen("output.txt", "w", stdout);

    int n;
    cin >> n;

    for (int i = 0; i < n; i++)
        solve();
}