void solve() {
    int n;
    cin >> n;

    int res = 0;
    for (; n; n /= 2)
        res = res * 2 + n % 2;

    cout << res << "\n";
}

int main() {
    freopen("input.txt", "r", stdin);
    freopen("output.txt", "w", stdout);

    int n;
    cin >> n;

    for (int i = 0; i < n; i++)
        solve();
}