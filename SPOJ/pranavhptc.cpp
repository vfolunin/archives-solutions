using namespace std;

void solve() {
    long long size, a, b;
    cin >> size >> a >> b;

    long long res = abs(a - b);
    res = min(res, size - res);

    cout << res << "\n";
}

int main() {
    freopen("input.txt", "r", stdin);
    freopen("output.txt", "w", stdout);

    int n;
    cin >> n;

    for (int i = 0; i < n; i++)
        solve();
}