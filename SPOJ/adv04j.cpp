void solve() {
    int n;
    cin >> n;

    int p = 1, res = 1;
    while (p < n) {
        p *= 2;
        res++;
    }

    cout << res << "\n";
}

int main() {
    freopen("input.txt", "r", stdin);
    freopen("output.txt", "w", stdout);

    int n;
    cin >> n;

    for (int i = 0; i < n; i++)
        solve();
}