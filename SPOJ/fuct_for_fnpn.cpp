#include <string>
using namespace std;

void solve() {
    int n;
    cin >> n;

    if (n < 1) {
        cout << "invalid\n";
        return;
    }

    for (int i = 1; i <= n; i++) 
        cout << i << (i + 1 <= n ? " " : "\n");
}

int main() {
    freopen("input.txt", "r", stdin);
    freopen("output.txt", "w", stdout);

    int n;
    cin >> n;

    for (int i = 0; i < n; i++)
        solve();
}