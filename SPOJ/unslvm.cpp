void solve(int test) {
    int n;
    cin >> n;

    cout << "Case " << test << ": ";
    if (n == 1)
        cout << "2\n";
    else
        cout << "4" << string(n - 2, '9') << "7\n";
}

int main() {
    freopen("input.txt", "r", stdin);
    freopen("output.txt", "w", stdout);

    int testCount;
    cin >> testCount;

    for (int test = 1; test <= testCount; test++)
        solve(test);
}