using namespace std;

void solve(int test) {
    int n, limit;
    cin >> n >> limit;

    cout << "Case " << test << ":";
    for (int i = n; i <= limit; i += n)
        cout << " " << i;
    cout << "\n";
}

int main() {
    freopen("input.txt", "r", stdin);
    freopen("output.txt", "w", stdout);

    int testCount;
    cin >> testCount;

    for (int test = 1; test <= testCount; test++)
        solve(test);
}